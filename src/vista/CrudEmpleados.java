    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // TODO add your handling code here:
       

       
       //aqui se busca empleado
     
       String cedula = UpdateCedula.getText();
      
       if(cedula.isEmpty()){
            JOptionPane.showMessageDialog(null, "Se requiere el numero de identidad del empleado para realizar su consulta");
       }
       else {
           Empleado emBus = new Empleado();
           emBus.setCedula(cedula);
            EmpleadoLogica pl = new EmpleadoLogica();
            pl.buscarEmpleado(emBus);

           //String ceNumero = new String(cedula);
               

                if (pl.buscarEmpleado(emBus) == null) {
                    JOptionPane.showMessageDialog(null, "El empleado ingresado no radica en la base de datos del sistema");

                } else {
String Estado = String.valueOf(pl.buscarEmpleado(emBus).getEstado());
                    
                  UpdateNombre.setText(pl.buscarEmpleado(emBus).getNombre());
                    UpdateApellido.setText(pl.buscarEmpleado(emBus).getApellidos());

                    UpdateCargo.setText(pl.buscarEmpleado(emBus).getCargo());
                    UpdateDireccion.setText(pl.buscarEmpleado(emBus).getDireccion());
                    UpdateEstado.setText(Estado);
                    UpdateTelefono.setText(pl.buscarEmpleado(emBus).getTelefono());
                    UpdateCorreo.setText(pl.buscarEmpleado(emBus).getCorreo());
                   UpdateContrase침a.setText(pl.buscarEmpleado(emBus).getContrasenia());
                   
                  

                }
            }
    }//GEN-LAST:event_BuscarActionPerformed

